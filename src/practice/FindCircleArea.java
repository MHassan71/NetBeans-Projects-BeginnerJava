public class FindCircleArea {
    
    public static void main (String []args){
        
        double radius ; // r represents radius
        radius = 2;
        double area;
        
        
        area = 3.1416 * Math.pow(radius, 2);
        
        System.out.println("Area of circle: " + area);
    
    }
    
    
}
