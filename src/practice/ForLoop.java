
package practice;

public class ForLoop {
    
    public static void main(String []args){
        
        // increament 
        for (int i = 2; i <= 20; i=i+2) {
            System.out.println(i);
            
        }
        System.out.println("====================");
        
        // decrement 
        for (int j = 100; j >0; j=j-10) {
            System.out.println(j);
            
        }
    }
    
}
